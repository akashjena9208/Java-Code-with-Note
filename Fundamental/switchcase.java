public class switchcase {

    public static void main(String[] args) {
        int num =300;
        switch(num){
            case 200:System.out.println("case 1");
            break;

            case 300:System.out.println("case 2");
            break;

            case 100: System.out.println("case 3");
            break; 
        }

    }
    
}

