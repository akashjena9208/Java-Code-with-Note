// package Array;

public class array5jugesarray {
    public static void main(String[] args) {
        // int num [][]= new int [3][2];
       int  num[][]={
                    {70,20,30,40},
                    {2,4,6,8},
                    {5,10}
        };

        for (int i=0;i<=2;i++)
        {
            for(int j=0;j<num[i].length;j++)
            {
                System.out.print(num[i][j] +" ");
            }
            System.out.println();
        }


    }
    
}


    
